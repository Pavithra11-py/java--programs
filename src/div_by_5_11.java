public class div_by_5_11 {
    public static void main(String[] args) {
        int num=66;
        if(num%5==0 && num%11==0){
            System.out.printf("%d is divisible by 5 and 11",num);
        }
        else{
            System.out.printf("%d is not divisible by 5 and 11",num);
        }
    }
}
